#include "libft.h"

size_t	ft_nbrlen_u(unsigned int nb)
{
	size_t		i;

	i = 0;
	if (nb == 0)
		return (1);
	while (nb)
	{
		nb /= 10;
		i++;
	}
	return (i);
}
